    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)courseTable.getModel();
        int selected_row = courseTable.getSelectedRow();
        Connection con=cn.ConnectDB();
        if(selected_row == -1)  JOptionPane.showMessageDialog(this, "Select a Row!");
        else{
            String id = (String) model.getValueAt(selected_row, 0);
            System.out.println(id);
            try {
                statement = con.createStatement();
                statement.executeUpdate("DELETE FROM involved WHERE involved.course_id="+id);
                statement.executeUpdate("DELETE FROM giving_by WHERE giving_by.course_id="+id);
                statement.executeUpdate("DELETE FROM enrolled_in WHERE enrolled_in.course_id="+id);
                statement.executeUpdate("DELETE FROM enrolled_in_the WHERE enrolled_in_the.course_id="+id);
                statement.executeUpdate("DELETE FROM course WHERE course.course_id="+id);
            } catch (SQLException ex) {
                Logger.getLogger(classInterface.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex);
            }
        }
        try {
            refleshList();
        } catch (SQLException ex) {
            Logger.getLogger(courseInterface.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

