    public void refleshList() throws SQLException{
        Connection con=cn.ConnectDB();
        
        DefaultTableModel model = (DefaultTableModel)courseTable.getModel();
        model.setRowCount(0);
        statement = con.createStatement();
        ResultSet rs = statement.executeQuery("SELECT course_name, course_id FROM course");
        //SELECT course.course_id , course.course_name FROM course JOIN giving_by ON course.course_id = giving_by.course_id JOIN staff ON giving_by.instructor_id = staff.staff_id JOIN enrolled_in_the ON course.course_id = enrolled_in_the.course_id JOIN class ON enrolled_in_the.class_id = class.class_id
        while(rs.next()){
            Object[] adding = {rs.getString("course_id"), rs.getString("course_name")};
            model.addRow(adding);
        }
        cn.DisconnectDB(con);
    
    }
