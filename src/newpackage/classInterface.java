    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        Connection con=cn.ConnectDB();
        DefaultTableModel model = (DefaultTableModel)classTable.getModel();
        int selected_row = classTable.getSelectedRow();
        String idC=null;
        if(selected_row == -1)  JOptionPane.showMessageDialog(this, "Select a Row!");
        else{
            String name = (String) model.getValueAt(selected_row, 0);
            try {
                statement = con.createStatement();
                System.out.println(name);
                ResultSet rs=statement.executeQuery("SELECT class_id FROM class WHERE class_name='"+name+"';");
                if(rs.next()){
                    idC = rs.getString("class_id");
                }
            } catch (SQLException ex) {
                Logger.getLogger(classInterface.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex);
            }
            try {
                statement = con.createStatement();
                statement.executeUpdate("DELETE FROM enrolled_in_the WHERE enrolled_in_the.class_id='" +idC+"'");
                statement.executeUpdate("DELETE FROM class WHERE class.class_id='" +idC+"'");
            } catch (SQLException ex) {
                Logger.getLogger(classInterface.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex);
            }
        }
        try {
            refleshList();
        } catch (SQLException ex) {
            Logger.getLogger(classInterface.class.getName()).log(Level.SEVERE, null, ex);
        }
        cn.DisconnectDB(con);
    }//GEN-LAST:event_deleteButtonActionPerformed

