    public void refleshList() throws SQLException{
        Connection con=cn.ConnectDB();
        
        DefaultTableModel model = (DefaultTableModel)classTable.getModel();
        model.setRowCount(0);
        statement = con.createStatement();
        ResultSet rs = statement.executeQuery("SELECT class_name,size FROM class");
        while(rs.next()){
            Object[] adding = {rs.getString("class_name"), rs.getString("size")};
            model.addRow(adding);
        }
        cn.DisconnectDB(con);
    }
    
    
    
